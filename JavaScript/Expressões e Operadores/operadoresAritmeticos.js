// Operadores aritméticos.
let n = 1


console.log("SOMA:", 1 + 1)
console.log("SUBTRAÇÃO:", 1 - 1)
console.log("MULTIPLICAÇÃO:", 1 * 1)
console.log("DIVISÃO:", 1 / 1)
console.log("RESTO:", 1 % 1)
console.log("EXPONENCIACAO:", 1 ** 1)


// Incrementa depois (por isso não mostra no console)
console.log("INCREMENTO:", n++)
console.log(n)

//Incrementa antes
console.log("INCREMENTO:", ++n)

//Incrementar mais de um (colocar)
n += 1
console.log(n)


//Decrementa depois (por isso não mostra no console)
console.log("DECREMENTO:", n--)
console.log(n)

//Decrementa antes
console.log("DECREMENTO:", --n)

//Incrementar mais de um (remover)
n -= 1
console.log(n)