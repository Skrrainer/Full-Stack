//Variables
let one = 1
let two = 2
let email = false
let password = true

// == Igual a
console.log("Igual a")
console.log(one == two)
console.log(one == 1)
console.log(one == "1")
// != Diferente de
console.log("Diferente de")
console.log(one != two)
console.log(one != 1)
console.log(one != "1")
// > Maior que
console.log("Maior que")
console.log(one > two)
console.log(one > 1)
console.log(one > "1")
// < Menor que
console.log("Menor que")
console.log(one < two)
console.log(one < 1)
console.log(one < "1")
// >= Maior ou igual a
console.log("Maior ou igual a")
console.log(one >= two)
console.log(one >= 1)
console.log(one >= "1")
// <= Menor ou igual a
console.log("Menor ou igual a")
console.log(one <= two)
console.log(one <= 1)
console.log(one <= "1")

//Estritamente igual a (tipo e valor, diferente dos anteriores)
console.log("Estritamente igual a")
console.log(one === two)
console.log(one === 1)
console.log(one === "1")
//Estritamente diferente de
console.log("Estritamente diferente de")
console.log(one !== two)
console.log(one !== 1)
console.log(one !== "1")

// AND (E) &&
console.log("AND (E)")
console.log(email && password)
// OR (OU) ||
console.log("OR (OU)")
console.log(email || password)
// NOT (Negação)
console.log("NOT (Negação)")
console.log(!email)
console.log(!password)